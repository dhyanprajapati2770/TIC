from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

urlpatterns = [
    path('admin/', admin.site.urls),
    
    # API URLs
    path('api/', include('healthcare.urls')),
    path('api/users/', include('users.urls')),
    path('api/symptoms/', include('symptoms.urls')),
    path('api/diseases/', include('diseases.urls')),
    path('api/medicines/', include('medicines.urls')),
    path('api/pharmacy/', include('pharmacy.urls')),
    path('api/dashboard/', include('dashboard.urls')),
    
    # JWT Authentication
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)


admin.site.site_title = "Admin site"
admin.site.site_header = "Recipe Dashboard"

admin.site.index_title = "The Independent Chef: Healthy Meals Made Easy for Students"